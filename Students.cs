class Student {
    int mssv;
    string name;
    string address;
    string tel;
    float score;

    Student(int mssv, string name, string address, string tel, float score)
    {
        this.mssv = mssv;
        this.name = name;
        this.address = address;
        this.tel = tel;
        this.score = score;

    }

}